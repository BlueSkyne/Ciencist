#calculo distancia recorrida expresada en Km en horas
def distancia_recorrida(velocidad, tiempo):
    distancia = velocidad * tiempo
    return distancia
#calculo de velocidad expresada en Km en horas    
def Velocidad_automovil(distancia, tiempo):
    Velocidad = distancia / tiempo
    return  Velocidad